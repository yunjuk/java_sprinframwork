package utils;

public class InitSampleData {
	private String[] ids;
	private String[] passwords;
	private String[] contacts;
	private String[] emails;
	private String[] registeredDates;
	private String[] modifiedDates;
	
	public String[] getIds() {
		return ids;
	}
	public void setIds(String[] ids) {
		this.ids = ids;
	}
	public String[] getPasswords() {
		return passwords;
	}
	public void setPasswords(String[] passwords) {
		this.passwords = passwords;
	}
	public String[] getContacts() {
		return contacts;
	}
	public void setContacts(String[] contacts) {
		this.contacts = contacts;
	}
	public String[] getEmails() {
		return emails;
	}
	public void setEmails(String[] emails) {
		this.emails = emails;
	}
	public String[] getRegisteredDates() {
		return registeredDates;
	}
	public void setRegisteredDates(String[] registeredDates) {
		this.registeredDates = registeredDates;
	}
	public String[] getModifiedDates() {
		return modifiedDates;
	}
	public void setModifiedDates(String[] modifiedDates) {
		this.modifiedDates = modifiedDates;
	}

}
